import pandas as pd

# إنشاء ملف الإكسيل مع الربط
with pd.ExcelWriter('التقارير_المالية_المترابطة.xlsx', engine='xlsxwriter') as writer:
    # 1. قائمة الدخل (Income Statement)
    df_income = pd.DataFrame({'البند': ['الإيرادات', 'تكلفة الإيرادات', 'مجمل الربح', 'مصروفات تشغيلية', 'الربح قبل الضريبة', 'ضريبة الدخل', 'صافي الربح'],
                              'القيمة': [1000, 600, 0, 100, 0, 50, 0]})
    df_income.to_excel(writer, sheet_name='قائمة الدخل', index=False)
    
    # 2. قائمة التدفقات النقدية (Cash Flow)
    df_cf = pd.DataFrame({'البند': ['الربح قبل الضريبة (من قائمة الدخل)', 'إهلاك', 'تغير في رأس المال العامل', 'صافي التدفق التشغيلي'],
                          'القيمة': [0, 50, -20, 0]})
    df_cf.to_excel(writer, sheet_name='التدفقات النقدية', index=False)
    
    # 3. الميزانية العمومية (Balance Sheet)
    df_bs = pd.DataFrame({'البند': ['نقدية (من التدفقات)', 'أصول أخرى', 'إجمالي الأصول', 'أرباح مرحلة (من الدخل)', 'حقوق ملكية أخرى', 'إجمالي الخصوم وحقوق الملكية'],
                          'القيمة': [0, 500, 0, 0, 400, 0]})
    df_bs.to_excel(writer, sheet_name='الميزانية العمومية', index=False)

    # إضافة المعادلات للربط التلقائي
    workbook  = writer.book
    
    # ربط التدفقات بقائمة الدخل
    sheet_cf = writer.sheets['التدفقات النقدية']
    sheet_cf.write_formula('B2', "='قائمة الدخل'!B6") # ترحيل الربح قبل الضريبة
    
    # ربط الميزانية بقائمة الدخل والتدفقات
    sheet_bs = writer.sheets['الميزانية العمومية']
    sheet_bs.write_formula('B2', "='التدفقات النقدية'!B5") # ترحيل النقدية النهائية
    sheet_bs.write_formula('B5', "='قائمة الدخل'!B8")      # ترحيل صافي الربح للأرباح المرحلة